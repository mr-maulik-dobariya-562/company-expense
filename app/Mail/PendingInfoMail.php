<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Mail\Mailables\Content;
use Illuminate\Mail\Mailables\Envelope;
use Illuminate\Queue\SerializesModels;

class PendingInfoMail extends Mailable
{
    use Queueable, SerializesModels;

    public $data; // Pass data to the email view

    /**
     * Create a new message instance.
     */
    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     */
    public function build()
    {
        return $this->from(explode(",", $this->data['store_email']), 'Maulik Dobariya')
                    ->subject('Pending Info Mail')
                    ->cc(explode(",", $this->data['store_cc_email']))
                    ->view('emails.pending-info') // Reference the email blade view
                    ->with('data', $this->data);
    }

